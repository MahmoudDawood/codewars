function toMountain(mat) {
  // Implement a function takes position and adds 1s till surrounding square is mountain
    // return true if made a change, else false
  function squareMax(x, y){
    let max = 0
    for(let i = Math.max(0, x - 1); i < Math.min(mat.length, x + 2); i++)
      for(let j = Math.max(0, y - 1); j < Math.min(mat[0].length, y + 2); j++){
        // console.log(mat[i][j])
        max = Math.max(max, mat[i][j])
      }
    return max
  }
  
  function check (x, y){
    let flag = false
    for(let i = Math.max(0, x - 1); i < Math.min(mat.length, x + 2); i++){
      for(let j = Math.max(0, y - 1); j < Math.min(mat[0].length, y + 2); j++){
        // while(Math.abs(mat[i][j] - mat[x][y]) > 1){
        const max = squareMax(i, j)
        if(max - mat[i][j] > 1){
          mat[i][j] = max - 1
          flag = true
        }
      }
    }
    return flag
  }

  let cnt = 0
  while(true){
    let flag = true
    for(let i = 0; i < mat.length; i+=2){
      for(let j = 0; j < mat[0].length; j+=2){
        if(check(i, j)){
          flag = false
        }
        console.log(++cnt)
      }
    }
    if(flag) break
  }
  return mat
}
